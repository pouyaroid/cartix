    public static function getAllIcons(): array
    {
        $all = [];
        foreach (self::getCategories() as $category) {
            foreach ($category['icons'] as $class => $meta) {
                $all[$class] = $meta;
            }
        }
        return $all;
    }

    public static function searchIcons(string $query): array
    {
        $query = strtolower(trim($query));
        if (empty($query)) return [];

        $results = [];
        $allIcons = self::getAllIcons();

        foreach ($allIcons as $class => $meta) {
            $name = strtolower($meta['name']);
            $keywords = array_map('strtolower', $meta['keywords']);

            if (str_contains($name, $query) || in_array($query, $keywords) || str_contains($class, $query)) {
                $results[$class] = $meta;
            }
        }

        return $results;
    }
